
function [] = write_numerical_results_line_to_latex_table( file_id, method_results_line, method_name , is_articulated, last_line)

fprintf( file_id, '\t\t\t \\textbf{%s - %s} & %.02f $\\pm$ %.02f & %.02f $\\pm$ %.02f & %.02f $\\pm$ %.02f & ', ...
    method_results_line.name, method_name, ...
    method_results_line.x_error, method_results_line.x_std, ...
    method_results_line.y_error, method_results_line.y_std, ...
    method_results_line.z_error, method_results_line.z_std);

fprintf( file_id, '%.02f $\\pm$ %.02f & %.02f $\\pm$ %.02f & %.02f $\\pm$ %.02f ', ...
    method_results_line.roll_error, method_results_line.roll_std, ...
    method_results_line.pitch_error, method_results_line.pitch_std, ...
    method_results_line.yaw_error, method_results_line.yaw_std);

if is_articulated == 1
    fprintf( file_id, '& %.02f $\\pm$ %.02f & %.02f $\\pm$ %.02f & %.02f $\\pm$ %.02f ', ...
    method_results_line.a1_error, method_results_line.a1_std, ...
    method_results_line.a2_error, method_results_line.a2_std, ...
    method_results_line.a3_error, method_results_line.a3_std);
end

if last_line == 0
    fprintf(file_id, '  \\\\ \\hline\n');
else
    fprintf(file_id, '\n');
end

end